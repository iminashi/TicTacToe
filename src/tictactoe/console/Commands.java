package tictactoe.console;

/**
 * Contains the one character commands used in the console Tic-Tac-Toe.
 *
 * @author Tapio Malmberg
 */
class Commands {
    /**
     * The command for changing the settings.
     */
    public static final char CHANGE_SETTINGS = 'c';

    /**
     * The command for quitting the game.
     */
    public static final char QUIT = 'q';

    /**
     * The command for resetting the game.
     */
    public static final char RESET = 'r';

    /**
     * The command for undoing the previous move.
     */
    public static final char UNDO = 'u';
}